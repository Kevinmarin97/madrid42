char	*ft_strcapitalize(char	*str)
{

	int i;
	int count;
	

	i = 0;
	count = 1;
	
	while(str[i] != '\0')
	{
		

		if((count == 1) || (str[i] - 1) >= 97 || (str[i] -1) <= 122)
		str[i] = (str[i] - 32);
		i++;
		count++;
		
	}
	return (0);
}